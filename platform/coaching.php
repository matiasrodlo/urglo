    function coachingType(calc)
    {
      if (calc == 1) 
      {
                if ($("#coaching-calc").is(":visible"))
                    $("#coaching-calc").fadeOut(200, function () { showCalculator(calc); });
                else
                    showCalculator(calc);
                if ( !document.getElementById("coaching1").classList.contains( "active"))
                document.getElementById("coaching1").classList.add( "active");
                updateCoachingPrice();
            }
      }

      function showCalculator(value) 
      {
        $("#coaching-calc").fadeIn(200);
      }

      function updateCoachingPrice() {
        var hours = parseInt($('#coaching-hours').val()) || 1;
        var currencyRate = 1;
        var currencySymbol = '$';
        var basePrice = 25;
        var totalPrice = basePrice * hours * currencyRate;
        var priceElement = $('#coaching-price-display');
        priceElement.addClass('updating');
        setTimeout(function() {
          if (totalPrice > 0) {
            $('#coaching-price').val(totalPrice.toFixed(0));
            var formattedPrice = currencySymbol + formatonumeropunto(totalPrice.toFixed(0));
            priceElement.text(formattedPrice);
          } else {
            $('#coaching-price').val("0");
            priceElement.text("$0");
          }
          priceElement.removeClass('updating');
        }, 150);
      }

      function formatonumeropunto(input) {
        var num = input;
        if(!isNaN(num)){
          num = num.toString().split('').reverse().join('').replace(/(?=\d*\.?)(\d{3})/g,'$1.');
          num = num.split('').reverse().join('').replace(/^[\.]/,'');
          return num;
        }
        return input;
      }


