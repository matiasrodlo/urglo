function updateCoachingHours() {
    var hours = document.getElementById('coaching-hours').value;
    var displayElement = document.getElementById('coaching-hours-value');
    
    displayElement.classList.add('updating');
    
    setTimeout(function() {
        displayElement.innerHTML = hours;
        displayElement.classList.remove('updating');
    }, 150);
}

function updateCoachingHoursSliderFill() {
    var slider = document.getElementById('coaching-hours');
    if (!slider) return;
    if (slider.getAttribute('data-touched') !== '1') return;

    var min = parseFloat(slider.min || '0');
    var max = parseFloat(slider.max || '100');
    var val = parseFloat(slider.value || '0');
    var pct = 0;
    if (max > min) {
        pct = ((val - min) / (max - min)) * 100;
    }
    pct = Math.max(0, Math.min(100, pct));

    slider.style.setProperty('--pct', pct + '%');
}

function handleCoachingHoursInput() {
    var slider = document.getElementById('coaching-hours');
    if (slider && slider.getAttribute('data-touched') !== '1') {
        slider.setAttribute('data-touched', '1');
    }
    updateCoachingHours();
    updateCoachingPrice();
    updateCoachingHoursSliderFill();
}

