    function getLeagueBoostingPrice(leagueFrom, divisionFrom, leagueTo, divisionTo, lp, lpg) {
        var totalPrice = parseFloat("0");
        var currLeague = leagueFrom;
        var currDivision = divisionFrom;

        var price = parseFloat("0");
        var tax = parseFloat("0");
        var discount = parseFloat("0");
        var lpcalculated = false;

        var currLine = getLeagueBoostingLine(currLeague, currDivision);

        while (currLeague != leagueTo || currDivision != divisionTo) {
            if (currLine != null &&
                currLine[3] != "" &&
                currLine[4] != "") {
                price = parseFloat(currLine[5]); // price index

                if (!lpcalculated) {

                    if (parseInt(lp) <= 20) {
                        discount = parseFloat(currLine[6]);
                    }
                    else if (parseInt(lp) > 20 && parseInt(lp) <= 40) {
                        discount = parseFloat(currLine[7]);
                    }
                    else if (parseInt(lp) > 40 && parseInt(lp) <= 60) {
                        discount = parseFloat(currLine[8]);
                    }
                    else if (parseInt(lp) > 60 && parseInt(lp) <= 80) {
                        discount = parseFloat(currLine[9]);
                    }
                    else if (parseInt(lp) > 80 && parseInt(lp) <= 100) {
                        discount = parseFloat(currLine[10]);
                    }

                    if (parseInt(lpg) >= 0 && parseInt(lpg) <= 3) {
                        tax = parseFloat(currLine[11]);
                    }
                    else if (parseInt(lpg) > 3 && parseInt(lpg) <= 7) {
                        tax = parseFloat(currLine[12]);
                    }
                    else if (parseInt(lpg) > 7 && parseInt(lpg) <= 11) {
                        tax = parseFloat(currLine[13]);
                    }
                    else if (parseInt(lpg) > 11 && parseInt(lpg) <= 14) {
                        tax = parseFloat(currLine[14]);
                    }
                    else if (parseInt(lpg) > 14) {
                        tax = parseFloat(currLine[15]);
                    }
                    lpcalculated = true;
                }
                else {
                    tax = 0;
                    discount = 0;
                }

                totalPrice = totalPrice + ((price + tax) - discount);

                currLeague = currLine[3];
                currDivision = currLine[4];
                currLine = getLeagueBoostingLine(currLeague, currDivision);
            }
            else {
                totalPrice = 0;
                break;
            }
        }

        return totalPrice;
    }

